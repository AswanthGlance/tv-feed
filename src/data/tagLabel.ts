export function tagLabel(t: string): string {
  return TAG_LABEL[t] || t.replace(/-/g, ' ');
}

// Build dynamic reaction options from a feed item's tags.
// L1 exit (positive=true): produces "More [world/scene]" expansion options.
// Dislike (positive=false): produces "Less [X]" options.
export function tagOpts(tg: string[], category: string, positive: boolean): Array<{ label: string; key: string }> {
  const skip = new Set([category, 'recipe', 'ambient-scenic', 'travel-card', 'food-story', 'cultural-story', 'sports-story', 'lifestyle-editorial', 'home-inspiration', 'product-story']);

  // For L1 exit: prefer sub-category and region tags for more specific "More…" labels
  // For dislike: use the same tags as "Less…" labels
  const out: string[] = [];
  for (const t of tg) {
    if (!skip.has(t) && !out.includes(t) && out.length < 3) out.push(t);
  }

  if (out.length === 0) {
    return [{ label: positive ? 'More like this' : 'Less like this', key: '' }];
  }

  const opts = out.map(t => ({
    label: (positive ? 'More ' : 'Less ') + tagLabel(t),
    key: t,
  }));

  // For positive L1 exit: always add a broad category option as the final choice
  if (positive) {
    const catLabel = TAG_LABEL[`cat:${category}`] || category;
    opts.push({ label: `More ${catLabel} stories`, key: `cat:${category}` });
  }

  return opts.slice(0, 4);
}
